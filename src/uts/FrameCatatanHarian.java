       private void initTable() {
        tableModel = new DefaultTableModel(new Object[]{"ID", "Judul", "Isi", "Tanggal"}, 0);
        jTable1.setModel(tableModel);
    }

    private void loadTableData() {
        tableModel.setRowCount(0); // Bersihkan data di tabel
        try (ResultSet rs = db.getAllNotes()) {
            while (rs != null && rs.next()) {
                tableModel.addRow(new Object[]{
                        rs.getInt("id"),
                        rs.getString("judul"),
                        rs.getString("isi"),
                        rs.getString("tanggal")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat memuat data: " + e.getMessage());
        }
    }

