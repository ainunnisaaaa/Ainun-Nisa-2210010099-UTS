    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       //Button Simpan
         String judul = JOptionPane.showInputDialog(this, "Masukkan Judul:");
        String isi = JOptionPane.showInputDialog(this, "Masukkan Isi:");
        String tanggal = JOptionPane.showInputDialog(this, "Masukkan Tanggal (YYYY-MM-DD):");

        if (judul != null && isi != null && tanggal != null) {
            db.addNote(judul, isi, tanggal);
            loadTableData();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //Button Ubah
         int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tableModel.getValueAt(selectedRow, 0);
            String judul = JOptionPane.showInputDialog(this, "Ubah Judul:", tableModel.getValueAt(selectedRow, 1));
            String isi = JOptionPane.showInputDialog(this, "Ubah Isi:", tableModel.getValueAt(selectedRow, 2));
            String tanggal = JOptionPane.showInputDialog(this, "Ubah Tanggal (YYYY-MM-DD):", tableModel.getValueAt(selectedRow, 3));

            if (judul != null && isi != null && tanggal != null) {
                db.updateNote(id, judul, isi, tanggal);
                loadTableData();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih catatan yang akan diubah!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       //Button Hapus
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) tableModel.getValueAt(selectedRow, 0);
            db.deleteNote(id);
            loadTableData();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih catatan yang akan dihapus!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

